name = "Jordan Avery"
age = 29
phone = "555-0148"
is_subscribed = True

print("=" * 30)
print(f"Name: {name}")
print(f"Age: {age}")
print(f"Phone: {phone}")
print(f"Subscribed: {is_subscribed}")
print("=" * 30)
print(f"Name type: {type(name)}")
print(f"Age type: {type(age)}")
print(f"Subscribed type: {type(is_subscribed)}")
